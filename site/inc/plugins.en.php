<h2>Overview of plugin capabilities</h2>

<p>Far Manager is so tightly integrated with its
plugins that it is simply meaningless to talk about Far and not to
mention the plugins. Plugins present an almost limitless expansion
of the features of Far.</p>

<p>Without going into details, some of the capabilities
can be noted:</p>

<ul>
  <li>syntax highlighting in program source texts.
  <li>working with FTP-servers (including access through proxy).
  <li>search and replace in many files at the same time, using regular
    expressions.
  <li>renaming groups of files with support for complex compound masks
    consisting of substitution symbols and templates.
  <li>NNTP/SMTP/POP3/IMAP4 clients and sending messages to a pager.
  <li>working with non-standard text screen resolutions.
  <li>conversion of texts from one national code page to another.
  <li>manipulating the contents of the Recycle Bin.
  <li>Process priority control on local or network PC.
  <li>Words autocomplete in editor and working with templates.
  <li>Windows system registry editing.
  <li>Creating and modifying Windows shortcuts.
  <li>File and text operations making it more comfortable to use FidoNet.
  <li>Files UU-encode and UU-decode.
  <li>WinAmp control and MP3-tags modifying.
  <li>Quake PAK-files processing.
  <li>Printers control, both connected to PC and network.
  <li>Connection and debugging of queries to ODBC-compatible
    databases.
  <li>RAS service control.
  <li>External programs executing (compilers, converters etc.) while
    editing text in Far editor.
  <li>Windows help files contents displaying (.hlp and .chm)
  <li>Calculators with different possibilities.
  <li>Several games :-)
  <li>Spell checker functions while editing text in Far editor.
  <li>Removable drives catalog preparation and much more...
</ul>

