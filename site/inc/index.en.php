<h2>Far Manager&nbsp;&#151; what is it?</h2>

	<p><table width="100%" border="0" cellpadding="0" cellspacing="0">
		<tr>
			<td width="70%" valign=top>
				<p>Far Manager is a program for managing files and archives in <acronym title="2000/XP/2003/Vista/2008/7">Windows operating systems</acronym>.
				Far Manager works in text mode and provides a simple and intuitive interface for performing
				most of the necessary actions:

				<ul>
					<li>viewing files and directories;
					<li>editing, copying and renaming files;
					<li>and many other actions.
				</ul>

			</td>
		</tr>
	</table>
</div>

<div>
	<h2>Customize it</h2>
	<p>Far Manager has a multi-language, easily configurable interface. File system navigation is made easier through the use of color highlighting and sort groups.
	<p>
</div>

<div>
	<h2>Your task&nbsp;&#151; your way</h2>
	<p>The functionality of Far Manager is greatly extended by external <acronym title="Dynamic-Link Library">DLL</acronym> modules&nbsp;&#151; plugins
	(made possible by a special set of interfaces&nbsp;&#151; the <a class="body_link" href="https://api.farmanager.com">Plugins&nbsp;API</a>).
	For example: archive support, FTP client, temporary panel and network browser are all implemented as plugins
	included in the standard distribution of Far.
